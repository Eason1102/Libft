#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*s1;

	if (!s || !f)
		return (NULL);
	s1 = malloc((ft_strlen((char *)s) + 1) * sizeof(char));
	if (!s1 || !s || !f)
		return (NULL);
	i = 0;
	while (s[i])
	{
		s1[i] = f(i, s[i]);
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
